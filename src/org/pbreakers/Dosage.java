package org.pbreakers;

class Dosage {
    private int nbJour;
    private int nbComprime;
    private int agePatient;
    private int nbFois;

    Dosage(){

    }

    public int getAgePatient() {
        return agePatient;
    }

    public int getNbComprime() {
        return nbComprime;
    }

    public int getNbFois() {
        return nbFois;
    }

    public int getNbJour() {
        return nbJour;
    }
}
